<h3 class="fluid-text monolisk" id="mixed">Mixed Flavors</h3>
<p style="padding-bottom: 2.5em;">Mixed flavors are color combinations, where a background, text, border, and outline color are pre-defined. Apply these classes to any element.</p>
<div class="shape point-left padded info" style="float:right; max-width: 33vw; margin-bottom: 4em;">
	<div class="bitstream" style="max-width: 75%; float: right;">
		<h4 class="flow-text">Color Override</h4>
		<p>To override a combo text color, apply a text color class directly to the element.</p>
		<pre class="black lime-t">&lt;p class="orange-cream"&gt;Orange<br />&nbsp;&nbsp;&lt;span class="blueberry-t"&gt;Blueberry&lt;/span&gt;<br />&lt;/p&gt;</pre>
		<p class="heavy">Output:</p>
		<p class="orange-cream flow-text">Orange <span class="blueberry-t">Blueberry</span></p>
		<hr />
		<h4 class="flow-text">Borders &amp; Outlines</h4>
		<p>Border colors are the same as the text color, and outline colors are the same as the background color. Consult <em>BUTCH</em> to see how to work with borders and outlines.</p>
