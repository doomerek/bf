<h3 class="fluid-text monolisk" id="stickies">Sticky Stacks</h3>
<p>Sticky Stacks are elements that use fixed positioning (sticky) and are set to a z-index (stacks).</p>
<div class="flex"><div class="padded half">
	<p class="padded disabled"><span class="flow-text bold">Sticky</span><br />Sticky elements are used to fixed position an element:<br />
	<strong>.glued</strong> <small>[sticky positions element at top of container]</small><br />
	<strong>.sticky</strong> <small>[fixed positions an element]</small><br />
	<strong>.sticky-b</strong> <small>[fixed position at bottom]</small><br />
	<strong>.sticky-b-r</strong> <small>[fixed position at bottom-right]</small><br />
	<strong>.sticky-b-l</strong> <small>[fixed position at bottom-left]</small><br />
	<strong>.sticky-t</strong> <small>[fixed position at top]</small><br />
	<strong>.sticky-t-r</strong> <small>[fixed position at top-right]</small><br />
	<strong>.sticky-t-l</strong> <small>[fixed position at top-left]</small><br /></p>
</div><div class="padded half">
	<p class="padded disabled"><span class="flow-text bold">Stacks</span><br />Stacks set an element to a z-index layer. Use any of these classes:<br />
	<strong>.stack-b</strong> <small>[lowest z-index using <em>--base</em> variable]</small><br />
	<strong>.stack-l</strong> <small>[low z-index using <em>--low</em> variable]</small><br />
	<strong>.stack-m</strong> <small>[middle z-index using <em>--mid</em> variable]</small><br />
	<strong>.stack-h</strong> <small>[high z-index using <em>--high</em> variable]</small></p>
</div></div>